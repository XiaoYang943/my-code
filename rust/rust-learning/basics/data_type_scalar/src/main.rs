// 标量（scalar）类型代表一个单独的值。Rust 有四种基本的标量类型：整型、浮点型、布尔类型和字符类型
fn main() {
    // 整型
    let a: i32 = 10; // 有符号 32 位整数
    let b: u64 = 100; // 无符号 64 位整数

    // 浮点型
    let x: f32 = 3.14; // 单精度浮点
    let y: f64 = 2.718281828; // 双精度浮点

    // 布尔类型
    let is_rust_fun: bool = true;
    let is_bug_free: bool = false;

    // 字符类型
    let c1: char = 'A';
    let c2: char = '中';
}
