fn main() {
    // ===================== Rust Copy / Move 总结 =====================
    //
    // 1. Rust 默认语义：赋值 / 传参 = move（所有权转移）
    //
    // 2. 只有当类型实现了 Copy trait 时：
    //    - 才不会发生 move
    //    - 而是隐式按位复制一个新值
    //    - 原变量在之后仍然有效
    //
    // 3. Copy 类型的直觉判断规则：
    //    - 不涉及堆内存
    //    - 不需要释放资源（没有 drop 语义）
    //    - 按位拷贝就是正确复制
    //
    // 4. 常见 Copy 类型：
    //    - 基本数值类型：i32 / u64 / f64 / bool / char 等
    //    - 只由 Copy 成员组成的元组、数组
    //    - 不可变引用 &T（地址本身是值）
    //
    // 5. 常见非 Copy 类型（会 move）：
    //    - String / Vec / Box / HashMap / Rc / Arc
    //    - &mut T（可变借用必须唯一，不能复制）
    //
    // 6. 自定义类型是否是 Copy：
    //    - 所有字段必须是 Copy
    //    - 且需显式 #[derive(Copy, Clone)]
    //
    // 一句话总结：
    // Copy = 可以被复制的小值
    // Move = 有所有权、需要被小心对待的资源
    // ================================================================

    println!("Hello, world!");
}
